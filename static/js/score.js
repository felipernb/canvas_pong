function Score(x, y) {
	this.points = 0;
	this.x = x;
	this.y = y;
}

Score.prototype.draw = function(ctx) {
	ctx.fillStyle = "rgba(255, 255, 255, 0.4)";
	var score = this.numbers[this.points];
	for (var i = 0; i < 5; i++) {
		for (var j = 0; j < 5; j++) {
			if (score[i][j]) {
				ctx.fillRect(this.x + j*10, this.y + i*10, 10, 10);
			}
		}
	}
};

